    {
        return [
            'name' => fake('en_ZA')->company(),
            'region' => fake()->randomElement(['Eastern Cape', 'Free State', 'Gauteng', 'KwaZulu-Natal', 'Limpopo', 'Mpumalanga', 'Northern Cape', 'North West', 'Western Cape']),
            'address' => fake('en_ZA')->address()
        ];
    }
}
